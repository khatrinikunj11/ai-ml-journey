print("Hey,I will convert the temperature from celsius to fahrenhiet and kelvin")
Temp_in_celsius = int(input("Enter temperature (in celsius) - ")) #Taking Input

Temp_in_fahrenhiet = (Temp_in_celsius*9/5)+32 #Converting Temperature from celsius to fahrenhiet
print("Converted Temperature in fahrenhiet - ",Temp_in_fahrenhiet) #Telling user temperature in fahrenhiet

Temp_in_kelvin = Temp_in_celsius+273.15 #Converting Temperature from celsius to kelvin
print("Converted Temperature in kelvin - ",Temp_in_kelvin) #Telling user temperature in kelvin



